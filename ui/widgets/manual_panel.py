
from PyQt5.QtWidgets import QWidget, QVBoxLayout, QTextBrowser
from PyQt5.QtCore import Qt

class ManualPanel(QWidget):
    def __init__(self, parent=None):
        super().__init__(parent)
        self.layout = QVBoxLayout(self)
        self.layout.setContentsMargins(20, 20, 20, 20)
        
        self.text_browser = QTextBrowser()
        self.text_browser.setOpenExternalLinks(True)
        self.text_browser.setStyleSheet("""
            QTextBrowser {
                background-color: #1E1E2E;
                color: #CDD6F4;
                border: 1px solid #45475A;
                border-radius: 8px;
                padding: 15px;
                font-family: "Segoe UI", sans-serif;
                font-size: 12pt;
            }
        """)
        
        html_content = """
        <!DOCTYPE html>
        <html>
        <head>
            <style>
                body { color: #CDD6F4; font-family: "Segoe UI", sans-serif; }
                h1 { color: #89B4FA; margin-bottom: 10px; }
                h2 { color: #F9E2AF; margin-top: 20px; margin-bottom: 5px; }
                h3 { color: #A6E3A1; margin-top: 15px; margin-bottom: 5px; }
                p { margin-bottom: 10px; line-height: 1.4; }
                ul { margin-bottom: 10px; }
                li { margin-bottom: 5px; }
                b { color: #FAB387; }
                .highlight { color: #CBA6F7; font-weight: bold; }
            </style>
        </head>
        <body>
            <h1>Инструкция по использованию приложения</h1>
            <p>Добро пожаловать в приложение для визуализации и исследования Венгерского алгоритма и стратегий сбора урожая.</p>

            <h2>1. Вкладка "Визуализация"</h2>
            <p>Здесь вы можете пошагово изучить работу Венгерского алгоритма на конкретной матрице.</p>
            
            <h3>Редактор матрицы (Слева сверху)</h3>
            <ul>
                <li><b>Размер (n):</b> Задайте размерность квадратной матрицы (от 2 до 1000).</li>
                <li><b>Режим:</b> Выберите <i>Минимизация</i> (поиск минимальной стоимости) или <i>Максимизация</i> (поиск максимальной прибыли).</li>
                <li><b>Случайно:</b> Заполняет матрицу случайными числами (равномерное распределение).</li>
                <li><b>Очистить:</b> Очищает все ячейки.</li>
                <li><b>Загрузить/Сохранить:</b> Импорт и экспорт матрицы в текстовый файл.</li>
            </ul>

            <h3>Панель управления (Слева снизу)</h3>
            <ul>
                <li><b>Старт:</b> Инициализирует алгоритм с текущей матрицей.</li>
                <li><b>Навигация:</b> Используйте кнопки <i>Назад</i> и <i>Вперед</i> для пошагового просмотра.</li>
                <li><b>Авто:</b> Запускает автоматическое выполнение. Нажмите повторно для паузы.</li>
                <li><b>Скорость анимации:</b> Регулирует скорость в автоматическом режиме.</li>
                <li><b>Показать решение:</b> Мгновенно выводит конечный результат в лог, минуя визуализацию.</li>
                <li><b>Лог:</b> Отображает текстовое описание текущего шага алгоритма.</li>
            </ul>

            <h3>Область визуализации (Справа)</h3>
            <p>Отображает текущее состояние матрицы. Цветами выделяются:</p>
            <ul>
                <li><span style="color: #F9E2AF;">★ Звезды</span> (выбранные нули)</li>
                <li><span style="color: #45475A;">Темный фон</span> (покрытые строки/столбцы)</li>
            </ul>

            <hr style="border: 1px solid #45475A;">

            <h2>2. Вкладка "Сравнение алгоритмов"</h2>
            <p>Этот раздел предназначен для проведения экспериментов и сравнения эффективности различных стратегий (Венгерский алгоритм, Жадная стратегия, Бережливая стратегия и их комбинации).</p>

            <h3>Настройка параметров (Слева)</h3>
            <ul>
                <li><b>Кол-во партий:</b> Размер матрицы (количество дней/полей).</li>
                <li><b>Эксперименты:</b> Количество прогонов для усреднения результатов (чем больше, тем точнее, но дольше).</li>
                <li><b>Этап смены стратегии:</b> Номер шага, на котором происходит переключение в комбинированных стратегиях.</li>
                <li><b>Суточная масса, Сахаристость, Деградация:</b> Параметры для генерации сценариев сбора урожая.</li>
            </ul>

            <h3>Запуск и результаты</h3>
            <p>Нажмите кнопку <b class="highlight">Запустить сравнение</b>. После завершения расчетов (следите за полосой загрузки) справа станут доступны результаты:</p>
            <ul>
                <li><b>График:</b> Показывает динамику накопления стоимости/прибыли по шагам.</li>
                <li><b>Гистограмма:</b> Сравнение итоговых результатов всех стратегий.</li>
                <li><b>Общие результаты:</b> Текстовый отчет с рекомендацией наилучшей эвристической стратегии (исключая точный Венгерский максимум).</li>
            </ul>
            
            <p><i>Используйте выпадающий список справа сверху для переключения между видами результатов.</i></p>
        </body>
        </html>
        """
        
        self.text_browser.setHtml(html_content)
        self.layout.addWidget(self.text_browser)
